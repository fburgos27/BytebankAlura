
public class EjemploCaracteres {

	public static void main(String[] args) {
		
		char caracter = 'a';
		System.out.println(caracter);
		
		caracter = 65 ;
		System.out.println(caracter);
		
		String palabra = "Alura cursos online";
		System.out.println(palabra);
		
		palabra = palabra + " 2020";
		System.out.println(palabra);
		
	}
}
